# Операции с числами
# Сложение
print(1 + 1)  # При операции будет целое число
print(1 + 2.0)  # При операции будет вещественное число
# Вычитание
print(2 - 1)
print(2 - 1.0)
# Деление
print(10 / 5)  # При делении всегда будет вещественное число
# Умножение
print(2 * 2)
# Возведение в степень
print(2 ** 3)
# Целочисленное деление (остаток отбрасывается)
print(10 // 3)
# Получение только остатка от деления
print(10 % 3)  # Если остатка нет, то вернется 0
